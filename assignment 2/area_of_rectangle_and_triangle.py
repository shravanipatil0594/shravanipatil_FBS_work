#area of rectangle and triangle
l = float(input('enter value for length : '))
b = float(input('enter value for breadth : '))

rect = l*b 

print(f'area of rectangle is : {rect}sq.cm')

#area of triangle
h = float(input('enter value for height : '))
b = float(input('enter value for base : '))

triangle = 0.5 * h * b

print(f'area of triangle is : {triangle}cm')
